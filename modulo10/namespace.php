<h3>Namespace</h3>
<p>O objetivo é determinar areas para que possam evitar conflitos de bibliotecas e funções, 
<BR/>além de poder deteminar apelidos para acesso rapido.
<BR/>similar ao package do JAVA, mas de forma virtual, é possível utilizar os diretorios como no JAVA para definir os Namespaces
</p>
<h4>Conflito</h4>
<p>Pode ser utilizado para evitar as duas funções conflitantes
<BR/>Finceiro\calc();
<BR/>Vendas\calc();
</p>
<h4>Alias</h4>
<p>Possibilidade de criar "apelidos", para que acesse de forma reduzida e rapida</p>
